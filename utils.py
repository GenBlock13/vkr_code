def generate_dataset(n_per_class: int = 400, seed: int = RANDOM_SEED) -> pd.DataFrame:
    """Генерация сбалансированного синтетического датасета (по умолчанию 400+400).

    Подлинные отзывы собираются комбинаторно из вариативных фрагментов
    (зачин + содержание + заключение), ботовые — из шаблонов с повторами,
    капсом и избыточной пунктуацией. Метка: 0 — подлинный, 1 — бот.
    """
    rng = random.Random(seed)
    n_hard = max(1, int(n_per_class * 0.18))  # доля «сложных» примеров в классе

    real, seen = [], set()
    while len(real) < n_per_class - n_hard:
        t = " ".join([
            rng.choice(_REAL_OPENINGS),
            rng.choice(_REAL_MIDDLES),
            rng.choice(_REAL_ENDINGS),
        ])
        if t not in seen:
            seen.add(t)
            real.append(t)
    while len(real) < n_per_class:  # пограничные подлинные: шаблон + личная деталь
        parts = rng.sample(_GENERIC, rng.randint(1, 2)) + [rng.choice(_PERSONAL)]
        rng.shuffle(parts)
        t = " ".join(parts)
        if t not in seen:
            seen.add(t)
            real.append(t)

    bots, seen_b = [], set()
    while len(bots) < n_per_class - n_hard:
        tpl = rng.choice(_BOT_TEMPLATES)
        x = rng.choice(_BOT_FILLERS_X)
        t = (tpl.replace("{x}", x)
                .replace("{X}", x.upper())
                .replace("{w}", rng.choice(_BOT_FILLERS_W))
             + rng.choice(_BOT_SUFFIXES))
        if t not in seen_b:
            seen_b.add(t)
            bots.append(t)
    while len(bots) < n_per_class:  # пограничные боты: только шаблонные фразы
        parts = rng.sample(_GENERIC, rng.randint(2, 4))
        t = " ".join(parts)
        if t not in seen_b:
            seen_b.add(t)
            bots.append(t)

    df = pd.DataFrame({
        "text": real + bots,
        "label": [0] * n_per_class + [1] * n_per_class,
    })
    df["label_name"] = df["label"].map({0: "подлинный", 1: "бот"})
    return df.sample(frac=1.0, random_state=seed).reset_index(drop=True)
