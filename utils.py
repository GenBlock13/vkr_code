def extract_features(text: str) -> dict:
    """Извлечение девяти лингвистических и статистических признаков из текста.

    Чистая функция: не имеет побочных эффектов, легко тестируется.
    """
    text = text or ""
    text_lower = text.lower()
    words = text_lower.split()
    sentences = re.split(r"[.!?]+", text)
    sentences = [s.strip() for s in sentences if s.strip()]

    unique_words = set(words)
    lexical_diversity = len(unique_words) / len(words) if words else 0.0
    caps_ratio = sum(1 for c in text if c.isupper()) / len(text) if text else 0.0
    exclamation_count = text.count("!")
    repeat_count = sum(1 for i in range(len(words) - 1) if words[i] == words[i + 1])
    avg_word_len = float(np.mean([len(w) for w in words])) if words else 0.0
    space_ratio = text.count(" ") / len(text) if text else 0.0
    sent_diversity = len(set(sentences)) / len(sentences) if sentences else 0.0

    return {
        "lexical_diversity": round(lexical_diversity, 4),
        "caps_ratio": round(caps_ratio, 4),
        "exclamation_count": exclamation_count,
        "repeat_count": repeat_count,
        "avg_word_len": round(avg_word_len, 4),
        "space_ratio": round(space_ratio, 4),
        "sent_diversity": round(sent_diversity, 4),
        "text_length": len(text),
        "word_count": len(words),
    }


